#include "libft.h"

int		ft_cntwrd(const char *s, char c)
{
	unsigned int	i;
	int				cnt;

	i = 0;
	cnt = 0;
	while (s[i])
	{
		while (s[i] == c)
			i++;
		if (s[i] != 0)
			cnt++;
		while (s[i] && s[i] != c)
			i++;
	}
	return (cnt);
}
